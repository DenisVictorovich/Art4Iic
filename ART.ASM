port_configure: ; (void)
  rcall   transmitt
    ; sbi  art_DDR,  art_T
    ; cbi  art_DDR,  art_R
    ; sbi  art_PORT, art_T
    ; sbi  art_PORT, art_R
      cbi  art_DDR,  art_R
      cbi  art_PORT, art_R
  rcall   receive
  ret

; -----------------------------------------------------------------------------------------------------------------------------

  art_R_pause:  var_delay_3_clc  art_R_pause_value
                nop
                nop
                ret

  art_R_shift_pause:  var_delay_3_clc  art_R_shift_pause_value
                      ret

  ; Generated by delay loop calculator
  ; at http://www.bretmulvey.com/avrdelay.html

  art_T_pause:  var_delay_3_clc  art_T_pause_value
                ret

; -----------------------------------------------------------------------------------------------------------------------------

  .MACRO art_R_bit
            rcall  art_R_pause
            ; ЧТЕНИЕ РАЗРЯДА `@0`
            sbic   art_PIN, art_R
            sbr    t_0, 1 << @0
  .ENDMACRO

  .MACRO art_T_bit
            ; ЗАПИСЬ РАЗРЯДА `@0`
            sbrc   t_1, @0
            sbi    art_PORT, art_T
            sbrs   t_1, @0
            cbi    art_PORT, art_T
            rcall  art_T_pause
  .ENDMACRO

; -----------------------------------------------------------------------------------------------------------------------------

check_receive: ; (void), return: if SREG.T=0, then received, result in `t_0`
     set
     clr    t_0
     sbic   art_PIN, art_R ; ловим стартовый бит ...
     ret ; высокий уровень
     rcall  art_R_shift_pause
     ; НАЧАЛО ПРИЁМА БАЙТА ДАННЫХ
     art_R_bit  0
     art_R_bit  1
     art_R_bit  2
     art_R_bit  3
     art_R_bit  4
     art_R_bit  5
     art_R_bit  6
     art_R_bit  7
     rcall  art_R_shift_pause
     clt
     ret

; -----------------------------------------------------------------------------------------------------------------------------

send: ; (char b{ t_1 })
     cbi    art_PORT, art_T
     rcall  art_T_pause
     ; `довесок`
     nop
     nop
     nop
     art_T_bit  0
     art_T_bit  1
     art_T_bit  2
     art_T_bit  3
     art_T_bit  4
     art_T_bit  5
     art_T_bit  6
     art_T_bit  7
     sbi    art_PORT, art_T
     rcall  art_T_pause
     rcall  art_T_pause
     rcall  art_T_pause
     ret

; -----------------------------------------------------------------------------------------------------------------------------

; Преобразовать число 0..15 в символ `0`..`9` `A`..`F`
d2s: ; (char v{ t_1 })
     subi    t_1, 208 ; t_1+='0'
     cpi     t_1, 58  ; if(t_1>'9'), carry set if ('9'+1)>t_1
     brcs    d2s_1    ; branch if carry set
     subi    t_1, 249 ; t_1+=7
  d2s_1:
     ret

; -----------------------------------------------------------------------------------------------------------------------------

; Преобразовать символ `0`..`9` `A`..`F`..`N` в число 0..15..23
s2d: ; (char v{ t_1 })
     subi    t_1, 48 ; t_1-='0'
     cpi     t_1, 10 ; if(t_1>9), carry set if (9+1)>t_1
     brcs    s2d_1   ; branch if carry set
     subi    t_1, 7  ; t_1-=7
  s2d_1:
     ret

; -----------------------------------------------------------------------------------------------------------------------------

send2: ; (char b{ t_2 })
       mov   t_1, t_2
       ; hi digit send
       swap  t_1 ; меняем местами "нибблы"
       andi  t_1, 15
       rcall d2s
       rcall send
       ; lo digit send
       mov   t_1, t_2
       andi  t_1, 15
       rcall d2s
       rcall send
       ret

; -----------------------------------------------------------------------------------------------------------------------------

sndstr: ; (char const* string{ RAMPZ:Z_h:Z_l })
      elpm  t_1, Z+
      tst   t_1
      breq  sndstr_1 ; eol
      rcall send
      rjmp  sndstr
   sndstr_1:
      ret

