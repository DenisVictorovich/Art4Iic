  .MACRO out_i ; Начало макроопределения.
            ldi  t_0, @1
            out  @0, t_0
  .ENDMACRO ; Конец макроопределения.

  .MACRO ldi_Z ; Начало макроопределения.
            ldi  ZH, high(@0<<1)
            ldi  ZL, low (@0<<1)
  .ENDMACRO ; Конец макроопределения.

  .MACRO ldi_Y ; Начало макроопределения.
            ldi  YH, high(@0)
            ldi  YL, low (@0)
  .ENDMACRO ; Конец макроопределения.

  .MACRO ldi_X ; Начало макроопределения.
            ldi  XH, high(@0)
            ldi  XL, low (@0)
  .ENDMACRO ; Конец макроопределения.

  .MACRO var_delay_3_clc ; Начало макроопределения.
            ldi   delay_c, @0
         var_delay_lbl:
            dec   delay_c
            brne  var_delay_lbl
  .ENDMACRO ; Конец макроопределения.

